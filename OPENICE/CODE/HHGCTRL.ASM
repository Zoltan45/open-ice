	MOVE	A14,*A8(OZVEL),L

	MOVE	A14,*A9(ODT_VEL),L
	MOVE	A14,*A9(OXVEL),L
	MOVE	A14,*A9(OZVEL),L

	MOVK	PM_GSMOTHER,A14
	MOVE	A14,*A6(POF_MODE),W
	CALLA	take_player_control
