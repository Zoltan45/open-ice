**************************************************************************
*								         *
* GET_TEAMMATE_BLOCKS							 *
* 									 *
* PASS:									 *
* A8 = PLAYER OBJECT							 *
* RETURN:								 *
* A0 = TEAMMATE BLOCK							 *
* A1 = TEAMMATE BLOCK							 *
*								         *
**************************************************************************

GET_TEAMMATE_BLOCKS
	MOVE	*A8(OID),A1,W
	SLL	24,A1
	SRL	28,A1
	SLL	6,A1
	ADDI	TEAMMATE_LIST-(020H*2),A1

	MOVE	*A1+,A0,L
	MOVE	*A1+,A1,L
	RETS

TEAMMATE_LIST
	.long	PLAYER_2_BLOCK,PLAYER_4_BLOCK
	.long	PLAYER_1_BLOCK,PLAYER_4_BLOCK
	.long	0,0
	.long	PLAYER_1_BLOCK,PLAYER_2_BLOCK
	.long	PLAYER_6_BLOCK,PLAYER_8_BLOCK
	.long	PLAYER_5_BLOCK,PLAYER_8_BLOCK
	.long	0,0
