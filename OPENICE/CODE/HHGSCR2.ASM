SET_GKO_SCRIPT
	MOVI	SGKS_TAB-32,A14
	CALLA	start_panim_if_not_ta14


	MOVE	*A8(ODT_GFLAGS),A14,W

	BTST	B_MISSSAVE,A14
	JRZ	SGKOS_CHECK_PUCK		;BR=AIN'T GOIN' TO MISS IT!

	ORI	M_SAVEMADE,A14
	MOVE	A14,*A8(ODT_GFLAGS),W

SGKOS_CHECK_PUCK
	MOVE	@PUCK_OBJECT,A9,L
	MOVE	*A9(ODT_VEL+010H),A1,W
	CMPI	31,A1
	JRLE	SGKOS_COMP			;BR=UNDER MAXIMUM SPEED
	MOVI	31,A1
SGKOS_COMP
	SUBK	24-1,A1
	MOVI	120/(31-24+1),A0
	MPYU	A0,A1
	MOVB	A1,*A8(ANIMSLP)
	RETS

