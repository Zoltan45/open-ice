	MOVE	*A8(ODT_GFLAGS),A14,W
	BTST	B_FETCHING,A14
	JRNZ	DRG_FP_CONTINUE			;BR=ALREADY STARTED GOING HOME

	ANDNI	M_GOHOME,A14
	ORI	M_FETCHING,A14
	MOVE	A14,*A8(ODT_GFLAGS),W

	CLR	A14
	MOVE	A14,*A8(ODT_VEL),L
	MOVE	A14,*A8(OXVEL),L
	MOVE	A14,*A8(OZVEL),L

