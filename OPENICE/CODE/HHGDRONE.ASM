	MOVI	RGHT_GOALLINE_X,A1
	SUB	A14,A1
	CMP	A1,A0
	JRLT	DRG_POSITION_TO_SAVE		;BR=TOO FAR

	MOVE	*A7(OZVAL),A0,L

	MOVI	CENTER_Z,A1
	SUB	A14,A1
	CMP	A1,A0
	JRLT	DRG_POSITION_TO_SAVE		;BR=TOO FAR

	ADD	A14,A1
	ADD	A14,A1
	CMP	A1,A0
