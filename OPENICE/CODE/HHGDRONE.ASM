	CLR	A4				;TURBO OFF

;	MOVE	B0,A0
;	MOVE	B1,A1

	MOVE	*A7(OXVAL),A0,L
	MOVE	*A7(OZVAL),A1,L

;	MOVE	*A8(ODT_PBK),A6,L

	MOVE	A0,A14
	ABS	A14
	CMPI	RGHT_GOALLINE_X,A14
	JRLE	DRG_FP_GETIT			;BR=PUCK IN FRONT OF GOAL LINE

	CMPI	BOT_GOALPOST_Z,A1
	JRGE	DRG_FP_TOP			;BR=PUCK BEHIND OR ABOVE GOAL

	MOVE	*A8(OZVAL),A14,L
