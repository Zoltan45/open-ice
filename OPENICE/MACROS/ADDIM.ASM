*ADDIM - ADD AN IMMEADIATE VALUE TO MEMORY.
*	ADDIM	IMMEADIATE,MEMORY,FIELD SIZE
*RETURNS:
*	N = 1 IF THE RESULT IS NEGATIVE
*	C = 1 IF THERE IS A CARRY
*	Z = 1 IF THE RESULT IS 0
*	V = 1 IF THERE IS AN OVERFLOW
ADDIM	.macro	IMM,MEM,FS
	MOVE	:MEM:,A14,:FS:
	ADDI	:IMM:,A14
	MOVE	A14,:MEM:,:FS:
	.endm

